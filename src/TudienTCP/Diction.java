package TudienTCP;

public class Diction {
    protected String TA,TV;

    public Diction() {
    }

    public Diction(String TA, String TV) {
        this.TA = TA;
        this.TV = TV;
    }

    public String getTA() {
        return TA;
    }

    public void setTA(String TA) {
        this.TA = TA;
    }

    public String getTV() {
        return TV;
    }

    public void setTV(String TV) {
        this.TV = TV;
    }

}
